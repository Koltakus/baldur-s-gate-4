class Players(SuperClass):
    def __init__(self, name, health, armor, attack):
        self.name = name
        self.health = health
        self.armor = armor
        self.attack = attack

class Warrior:
    def __init__(self, name, ultra_attack):
        self.name = warrior
        self.ultra_attack = ultra_attack
        super().__init__(name, health, armor, attack)

class Rouge:
    def __init__(self, name, ultra_skip):
        self.name = Rouge
        self.ultra_attack = ultra_skip
        super().__init__(name, health, armor, attack)

